public class FloatMath {
    public static float cos(float t) {
        return (float)Math.cos(t);
    }

    public static float sin(float t) {
        return (float)Math.sin(t);
    }

    public static float sqrt(float t) {
        return (float)Math.sqrt(t);
    }
}
